package Chapter1;
/**
 * Program to display the perimeter and area of a circle
 *
 * @author Mika Nave
 */
public class C1_8 {
/**
* Main Method 
*
* @param args arguments from command line prompt
*/    
    public static void main(String[] args){
        //Solves and displays the circles perimeter
        System.out.print("(2 * 5.5 * Math.PI) is ");
        System.out.println("perimeter = " + (2*5.5*Math.PI));
        //Solves and displays the circles area
        System.out.print("(5.5 * 5.5 * Math.PI) is ");
        System.out.println("area = " + (5.5*5.5*Math.PI));
    }

}
